-- Row count check
INSERT INTO PROJECT3_DQ_MONITORING.DQ_RULE_RESULTS (
    RUN_ID,
    RULE_NAME,
    TARGET_TABLE,
    RULE_TYPE,
    RULE_STATUS,
    EXPECTED_VALUE,
    ACTUAL_VALUE
)
SELECT
    (SELECT MAX(RUN_ID) FROM PROJECT3_DQ_MONITORING.DQ_PIPELINE_RUNS),
    'Row Count Check',
    'RAW_WORKFORCE_API_DATA',
    'ROW_COUNT',
    CASE WHEN COUNT(*) > 0 THEN 'PASS' ELSE 'FAIL' END,
    '> 0',
    COUNT(*)
FROM RAW_WORKFORCE_API_DATA;

-- Null check
INSERT INTO PROJECT3_DQ_MONITORING.DQ_RULE_RESULTS (
    RUN_ID,
    RULE_NAME,
    TARGET_TABLE,
    RULE_TYPE,
    RULE_STATUS,
    EXPECTED_VALUE,
    ACTUAL_VALUE
)
SELECT
    (SELECT MAX(RUN_ID) FROM PROJECT3_DQ_MONITORING.DQ_PIPELINE_RUNS),
    'Critical Null Check',
    'RAW_WORKFORCE_API_DATA',
    'NULL_CHECK',
    CASE
        WHEN
            SUM(CASE WHEN SERIES_ID IS NULL THEN 1 ELSE 0 END) +
            SUM(CASE WHEN YEAR IS NULL THEN 1 ELSE 0 END) +
            SUM(CASE WHEN PERIOD IS NULL THEN 1 ELSE 0 END) +
            SUM(CASE WHEN METRIC_VALUE IS NULL THEN 1 ELSE 0 END)
        = 0
        THEN 'PASS'
        ELSE 'FAIL'
    END,
    '0 NULLS',
    SUM(CASE WHEN SERIES_ID IS NULL THEN 1 ELSE 0 END) +
    SUM(CASE WHEN YEAR IS NULL THEN 1 ELSE 0 END) +
    SUM(CASE WHEN PERIOD IS NULL THEN 1 ELSE 0 END) +
    SUM(CASE WHEN METRIC_VALUE IS NULL THEN 1 ELSE 0 END)
FROM RAW_WORKFORCE_API_DATA;

-- Duplicate check
INSERT INTO PROJECT3_DQ_MONITORING.DQ_RULE_RESULTS (
    RUN_ID,
    RULE_NAME,
    TARGET_TABLE,
    RULE_TYPE,
    RULE_STATUS,
    EXPECTED_VALUE,
    ACTUAL_VALUE
)
SELECT
    (SELECT MAX(RUN_ID) FROM PROJECT3_DQ_MONITORING.DQ_PIPELINE_RUNS),
    'Duplicate Key Check',
    'RAW_WORKFORCE_API_DATA',
    'DUPLICATE_CHECK',
    CASE WHEN COUNT(*) = 0 THEN 'PASS' ELSE 'FAIL' END,
    '0 DUPLICATES',
    COUNT(*)
FROM (
    SELECT SERIES_ID, YEAR, PERIOD, COUNT(*) AS DUP_COUNT
    FROM RAW_WORKFORCE_API_DATA
    GROUP BY SERIES_ID, YEAR, PERIOD
    HAVING COUNT(*) > 1
);

-- Numeric validation check
INSERT INTO PROJECT3_DQ_MONITORING.DQ_RULE_RESULTS (
    RUN_ID,
    RULE_NAME,
    TARGET_TABLE,
    RULE_TYPE,
    RULE_STATUS,
    EXPECTED_VALUE,
    ACTUAL_VALUE
)
SELECT
    (SELECT MAX(RUN_ID) FROM PROJECT3_DQ_MONITORING.DQ_PIPELINE_RUNS),
    'Numeric Metric Validation',
    'RAW_WORKFORCE_API_DATA',
    'NUMERIC_CHECK',
    CASE WHEN COUNT(*) = 0 THEN 'PASS' ELSE 'FAIL' END,
    '0 INVALID VALUES',
    COUNT(*)
FROM RAW_WORKFORCE_API_DATA
WHERE TRY_TO_NUMBER(METRIC_VALUE) IS NULL
